    public void SATable(){
        ArrayList <User1> list1 = getUserList();
        DefaultTableModel model = (DefaultTableModel) jTableView.getModel();
        Object[] row = new Object[2];
        for (int i = 0;i<list1.size();i++){
            row[0] = list1.get(i).getAnsware();
            row[1] = list1.get(i).getId();
            model.addRow(row);
        }
    }
    
    
    
    
   
    
    
    
    
    
    //execute the query
    public void executeSQlQuery(String query , String message) throws SQLException{
        Connection con = getConnection();
        Statement st;
        try{
            st = con.createStatement();
            if ((st.executeUpdate(query)) == 1){
                
                
                //refresh query.......
                DefaultTableModel model = (DefaultTableModel)jTableView.getModel();
                model.setRowCount(0);
                SATable();
                
                
                JOptionPane.showMessageDialog(null,"Data "+message+"Successfully");
            }
            else{
                JOptionPane.showMessageDialog(null,"Data Not "+message);
            }
       
        }
        catch(Exception ex){  
            ex.printStackTrace();
        }
    }


