    public ArrayList<User1> getUserList(){
        ArrayList<User1> userList1 = new ArrayList<User1>();
        Connection connection = getConnection();
        
        String query  = "select * from `SA` ";
        
        Statement st;
        ResultSet rs;
      //  ResultSet rr;
        try{
            st = connection.createStatement();
            rs = st.executeQuery(query);
            User1 user1;
            while (rs.next()){
                user1 = new User1(rs.getString("an"),rs.getInt("ID"));
                userList1.add(user1);
            }
        }
        catch(Exception e){
            e.printStackTrace();
        }
        return userList1;
    }
